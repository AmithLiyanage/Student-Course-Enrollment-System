
package takehomeassignment;

/**
 *
 * @author Amith
 */
public class Instructor {
    private int insID;
    private String insFullName;
    private String insName;
    private String nic;
    private String gender;
    private String address;
    private String qualification;
    private int contactNo;
    private String email;

    /**
     * @return the insID
     */
    public int getInsID() {
        return insID;
    }

    /**
     * @param insID the insID to set
     */
    public void setInsID(int insID) {
        this.insID = insID;
    }

    /**
     * @return the insFullName
     */
    public String getInsFullName() {
        return insFullName;
    }

    /**
     * @param insFullName the insFullName to set
     */
    public void setInsFullName(String insFullName) {
        this.insFullName = insFullName;
    }

    /**
     * @return the insName
     */
    public String getInsName() {
        return insName;
    }

    /**
     * @param insName the insName to set
     */
    public void setInsName(String insName) {
        this.insName = insName;
    }

    /**
     * @return the nic
     */
    public String getNic() {
        return nic;
    }

    /**
     * @param nic the nic to set
     */
    public void setNic(String nic) {
        this.nic = nic;
    }

    /**
     * @return the address
     */
    public String getAddress() {
        return address;
    }

    /**
     * @param address the address to set
     */
    public void setAddress(String address) {
        this.address = address;
    }

    /**
     * @return the qualification
     */
    public String getQualification() {
        return qualification;
    }

    /**
     * @param qualification the qualification to set
     */
    public void setQualification(String qualification) {
        this.qualification = qualification;
    }

    /**
     * @return the contactNo
     */
    public int getContactNo() {
        return contactNo;
    }

    /**
     * @param contactNo the contactNo to set
     */
    public void setContactNo(int contactNo) {
        this.contactNo = contactNo;
    }

    /**
     * @return the email
     */
    public String getEmail() {
        return email;
    }

    /**
     * @param email the email to set
     */
    public void setEmail(String email) {
        this.email = email;
    }

    /**
     * @return the gender
     */
    public String getGender() {
        return gender;
    }

    /**
     * @param gender the gender to set
     */
    public void setGender(String gender) {
        this.gender = gender;
    }
    
}
