public class Lecturer {
    private int lecID;
    private String lecFullName;
    private String lecName;
    private String nic;
    private String gender;
    private String address;
    private String qualification;
    private int contactNo;
    private String email;
    private String whatCoursesDo;

    /**
     * @return the lecID
     */
    public int getLecID() {
        return lecID;
    }

    /**
     * @param lecID the lecID to set
     */
    public void setLecID(int lecID) {
        this.lecID = lecID;
    }

    /**
     * @return the lecFullName
     */
    public String getLecFullName() {
        return lecFullName;
    }

    /**
     * @param lecFullName the lecFullName to set
     */
    public void setLecFullName(String lecFullName) {
        this.lecFullName = lecFullName;
    }

    /**
     * @return the lecName
     */
    public String getLecName() {
        return lecName;
    }

    /**
     * @param lecName the lecName to set
     */
    public void setLecName(String lecName) {
        this.lecName = lecName;
    }

    /**
     * @return the nic
     */
    public String getNic() {
        return nic;
    }

    /**
     * @param nic the nic to set
     */
    public void setNic(String nic) {
        this.nic = nic;
    }

    /**
     * @return the address
     */
    public String getAddress() {
        return address;
    }

    /**
     * @param address the address to set
     */
    public void setAddress(String address) {
        this.address = address;
    }

    /**
     * @return the qualification
     */
    public String getQualification() {
        return qualification;
    }

    /**
     * @param qualification the qualification to set
     */
    public void setQualification(String qualification) {
        this.qualification = qualification;
    }

    /**
     * @return the contactNo
     */
    public int getContactNo() {
        return contactNo;
    }

    /**
     * @param contactNo the contactNo to set
     */
    public void setContactNo(int contactNo) {
        this.contactNo = contactNo;
    }

    /**
     * @return the email
     */
    public String getEmail() {
        return email;
    }

    /**
     * @param email the email to set
     */
    public void setEmail(String email) {
        this.email = email;
    }

    /**
     * @return the whatCoursesDo
     */
    public String getWhatCoursesDo() {
        return whatCoursesDo;
    }

    /**
     * @param whatCoursesDo the whatCoursesDo to set
     */
    public void setWhatCoursesDo(String whatCoursesDo) {
        this.whatCoursesDo = whatCoursesDo;
    }

    /**
     * @return the gender
     */
    public String getGender() {
        return gender;
    }

    /**
     * @param gender the gender to set
     */
    public void setGender(String gender) {
        this.gender = gender;
    }
}
